@router.callback_query(F.data.startswith("menu_"))
async def process_menu_callback(callback_query: CallbackQuery):
    """
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –Ω–∞–∂–∞—Ç–∏—è –∫–Ω–æ–ø–æ–∫ –≤ –º–µ–Ω—é.

    –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç –∫–∞—Ç–µ–≥–æ—Ä–∏—é –ø–æ –ø–æ–ª—É—á–µ–Ω–Ω—ã–º –¥–∞–Ω–Ω—ã–º –∏ –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é —Ç–µ–∫—Å—Ç –º–µ–Ω—é —Å –∫–Ω–æ–ø–∫–∞–º–∏.

    Args:
        callback_query (CallbackQuery): –û–±—ä–µ–∫—Ç –Ω–∞–∂–∞—Ç–∏—è –∫–Ω–æ–ø–∫–∏ –≤ –º–µ–Ω—é.
    """
    category = callback_query.data.split("_")[1]
    menu_text = get_menu_text(category)
    await callback_query.message.edit_text(
        text=menu_text,
        reply_markup=await kb.menu(category)
    )


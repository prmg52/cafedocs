class ProductFilter(BaseFilter):
    """
    –§–∏–ª—å—Ç—Ä –¥–ª—è –æ–±—Ä–∞–±–æ—Ç–∫–∏ –≤—ã–±–æ—Ä–∞ —Ç–æ–≤–∞—Ä–æ–≤.

    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, —Å–æ–¥–µ—Ä–∂–∏—Ç—Å—è –ª–∏ –≤—ã–±—Ä–∞–Ω–Ω—ã–π —Ç–æ–≤–∞—Ä –≤ —Å–ø–∏—Å–∫–µ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö —Ç–æ–≤–∞—Ä–æ–≤.
    """

    def __init__(self, products: dict):
        self.products = products

    async def __call__(self, callback: CallbackQuery) -> bool:
        """
        –ü—Ä–æ–≤–µ—Ä—è–µ—Ç, –æ—Ç–Ω–æ—Å–∏—Ç—Å—è –ª–∏ –≤—Ö–æ–¥—è—â–∏–π callback –∫ –¥–æ—Å—Ç—É–ø–Ω—ã–º —Ç–æ–≤–∞—Ä–∞–º.

        Args:
            callback (CallbackQuery): –í—Ö–æ–¥—è—â–∏–π callback

        Returns:
            bool: True, –µ—Å–ª–∏ —Ç–æ–≤–∞—Ä –Ω–∞–π–¥–µ–Ω –≤ —Å–ø–∏—Å–∫–µ, –∏–Ω–∞—á–µ False.
        """
        data = callback.data
        if data.startswith("selected_"):
            product_name = data[len("selected_"):]  # –£–±–∏—Ä–∞–µ–º –ø—Ä–µ—Ñ–∏–∫—Å "selected_"
            product_name = product_name.replace('_', ' ')
            return product_name in self.products  # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –∫–ª—é—á –≤ —Å–ª–æ–≤–∞—Ä–µ
        return False


